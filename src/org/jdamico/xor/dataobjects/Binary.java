package org.jdamico.xor.dataobjects;

public class Binary {
	
	private int[] bitArray = new int[4];
	private String strBitArray;
	
	
	public int[] getBitArray() {
		return bitArray;
	}

	public void setBitArray(int[] bitArray) {
		this.bitArray = bitArray;
	}

	public String getStrBitArray() {
		return strBitArray;
	}

	public void setStrBitArray(String strBitArray) {
		this.strBitArray = strBitArray;
	}

	
	
}
